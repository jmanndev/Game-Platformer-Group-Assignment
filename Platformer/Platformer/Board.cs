        enum ETileTypes
        {
            NONE,
            TILE
        }

        public Board(SpriteBatch sp, Texture2D tileTex, int columns, int rows)
        {
            this.columns = columns;
            this.rows = rows;
            this.tileTexture = tileTex;
            this.spriteBatch = sp;

            tileGrid = new Tile[columns, rows];

            CreateBoard();
            CurrentBoard = this;
        }

        void CreateBoard()
        {
            for (int x = 0; x < rows; x++)
            {
                for (int y = 0; y < columns; y++)
                {
                    Vector2 tilePosition = new Vector2(x * tileTexture.Width, y * tileTexture.Height);
                    if (setupGrid[y, x] == (int)ETileTypes.NONE)
                    {
                        tileGrid[y, x] = new Tile(tileTexture, tilePosition, spriteBatch, false);
                    }
                    else if (setupGrid[y, x] == (int)ETileTypes.TILE)
                    {
                        tileGrid[y, x] = new Tile(tileTexture, tilePosition, spriteBatch, true);
                    }

                }
            }
        }

        void CreateTopBlocks()
        {

        }


        public void Draw()
        {
            foreach (var tile in tileGrid) {
                tile.Draw();
            }
        }

        public bool checkTileCollision(Rectangle recToCheck)
        {
            foreach(var tile in tileGrid)
            {
                if (recToCheck.Intersects(tile.rectangle) && tile.isBlocked)
                    return true;
            }
            
            return false;
        }
